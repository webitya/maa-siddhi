// Helper functions
export const getBlogById = (id) => {
  return blogPosts.find((post) => post.id === Number.parseInt(id))
}

export const getBlogBySlug = (slug) => {
  return blogPosts.find((post) => post.slug === slug)
}

export const getBlogsByCategory = (categoryId) => {
  return blogPosts.filter((post) => post.category === categoryId)
}

export const getFeaturedBlogs = () => {
  return blogPosts.filter((post) => post.featured)
}

export const getRecentBlogs = (limit = 5) => {
  return blogPosts.sort((a, b) => new Date(b.publishedAt) - new Date(a.publishedAt)).slice(0, limit)
}

export const getAllBlogs = () => {
  return blogPosts
}

export const searchBlogs = (query) => {
  const searchTerm = query.toLowerCase()
  return blogPosts.filter(
    (post) =>
      post.title.toLowerCase().includes(searchTerm) ||
      post.excerpt.toLowerCase().includes(searchTerm) ||
      post.tags.some((tag) => tag.toLowerCase().includes(searchTerm)) ||
      post.seoKeywords.some((keyword) => keyword.toLowerCase().includes(searchTerm)),
  )
}
